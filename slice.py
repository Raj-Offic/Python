##basics for slicing

list1=[1,2,3,4,'apple', 'banana', 'mango']

strings ='abcdefghijklmnopqrstuvwxyz1234567890'

print(strings[0])


print(strings[::4])  #slice operator prints the string every four letter


print(strings[:4]) # prints till the (n-1) th term


print(strings[4:])  # prints from the n th term


print(strings[4::]) # same as the previous one : )

print(strings[2:9]) # prints the text inbetween


